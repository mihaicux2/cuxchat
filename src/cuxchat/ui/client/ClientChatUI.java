    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(ClientChatUI.this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File myFile = fc.getSelectedFile();
            byte [] mybytearray  = new byte [(int)myFile.length()];
            FileInputStream fis;
            try {
                fis = new FileInputStream(myFile);
                fis.read(mybytearray);
                fis.close();
                int kbyte32 = 1024*32;
                
                Double chunks = Math.ceil(mybytearray.length / kbyte32);
                if (chunks.intValue() < 1)
                    chunks = Double.parseDouble("1");
                
                for (int i = 0; i < chunks; i++){
                    int start = kbyte32 * i;
                    int end   = kbyte32 * (i+1);
                    if (end > mybytearray.length) end = mybytearray.length;
                    System.out.println(start+", "+end+" : "+chunks + "[ "+mybytearray.length+" ]");
                    CuxMessage msg = new CuxMessage("FILE");
                    msg.put("file_to", endpoint);
                    msg.put("file_name", myFile.getName());
                    msg.put("file_type", ClientRegisterUI.getFileExtension(myFile));
                    msg.put("file_chunks", chunks.intValue());                
                    msg.put("file_chunk_no", i+1);
                    msg.put("file_part", Arrays.copyOfRange(mybytearray, start, end));
                    msg.put("file_bytes", mybytearray.length);
                    CuxStringEncrypter desEncrypter = new CuxStringEncrypter();
                    this.client.out.out.writeUTF(desEncrypter.encrypt(msg.toString()));
                }
                
            } catch (FileNotFoundException ex) {
                CuxLogger.getInstance().logException2(ex);
            } catch (IOException ex) {
                CuxLogger.getInstance().logException2(ex);
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    

