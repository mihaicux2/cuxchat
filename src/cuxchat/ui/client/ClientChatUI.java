    public void sendPrivateMessage(){
        String cmd = commandText.getText().trim();
        String style = "font-size: "+fontSize+"px; background: #"+backgroundColor+"; color: #"+foregroundColor+";";
        if (isBold){
            style += "font-weight: bold;";
        }
        if (isItalic){
            style += "font-style: italic;";
        }
        if (isUnderline){
            style += "text-decoration: underline;";
        }
        cmd = "<span style='"+style+"'>"+cmd+"</span>";
        try {
            CuxStringEncrypter desEncrypter = new CuxStringEncrypter();
            CuxMessage msg = new CuxMessage("MESSAGE");
            msg.put("message", "/msg "+endpoint+" "+cmd);
            this.client.out.out.writeUTF(desEncrypter.encrypt(msg.toString()));
            commandText.setText("");
            commandText.setCaretPosition(0);
            kit.insertHTML(doc, doc.getLength(), "you : "+cmd, 0, 0, null);
            outputTxt.setCaretPosition(doc.getLength());
            commandText.requestFocus();
        } catch (IOException ex) {
            //                ex.printStackTrace();
            ClientCommands.updateLog(ex.getMessage());
            CuxLogger.getInstance().logException2(ex);
        } catch (BadLocationException ex) {
            ClientCommands.updateLog(ex.getMessage());
            CuxLogger.getInstance().logException2(ex);
        }
    }
    
